    private void sendDataToController() {
        // Get the values from the fields
        String bookName = bnTF.getText().trim();
        String isbn = isbnTF.getText().trim();
        String authorName = anTF.getText().trim();
        String category = (String) catCMB.getSelectedItem();
        String copies = copiesTF.getText().trim();

        // Check if any of the fields are blank
        if (bookName.isEmpty() || isbn.isEmpty() || authorName.isEmpty() || category == null || copies.isEmpty()) {
            // Show an error message
            JOptionPane.showMessageDialog(null, "All fields must be filled out", "Input Error", JOptionPane.ERROR_MESSAGE);
        } else {
            // Send data to the controller
            BookController.Books(bookName, isbn, authorName, category, copies);
        }
    }
    
