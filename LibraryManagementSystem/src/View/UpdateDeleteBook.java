    private void getBookDataBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getBookDataBtnActionPerformed
        try {
            String ISBN = isbnTF.getText().trim();

            if (ISBN == null) {
                JOptionPane.showMessageDialog(this, "Please Type a ISBN.", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }

            ResultSet resultSet = dbSearch.searchBookDetailsByID(ISBN);

            if (resultSet != null && resultSet.next()) {
                bnTF.setText(resultSet.getString("bName"));
                anTF.setText(resultSet.getString("aName"));
                catCMB.setSelectedItem(resultSet.getString("category"));
                copiesTF.setText(resultSet.getString("copies"));

            } else {
                JOptionPane.showMessageDialog(this, "No book found for the Typed ISBN.", "No Data", JOptionPane.INFORMATION_MESSAGE);
            }

            if (resultSet != null && !resultSet.isClosed()) {
                resultSet.close();
            }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "An error occurred while searching for books.", "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_getBookDataBtnActionPerformed

    private void DeleteBookBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBookBtnActionPerformed
        String ISBN = isbnTF.getText().trim();

        if (ISBN.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Book ISBN must be filled out",
                "Input Error", JOptionPane.ERROR_MESSAGE);
        } else {
            // Confirm with the user before deleting
            int confirmation = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this book Details?",
                "Confirm Deletion", JOptionPane.YES_NO_OPTION);

            // If user confirms deletion
            if (confirmation == JOptionPane.YES_OPTION) {
                try {
                    boolean success = BookController.deleteBook(ISBN);

                    if (success) {
                        JOptionPane.showMessageDialog(null, "Book Details deleted successfully",
                            "Deletion Successful", JOptionPane.INFORMATION_MESSAGE);
                        // Clear the input fields after successful deletion
                        clearField();
                    } else {
                        JOptionPane.showMessageDialog(null, "Failed to delete book",
                            "Deletion Failed", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (SQLException ex) {
                    ex.printStackTrace();
                    // Handle SQL exception
                    JOptionPane.showMessageDialog(null, "An error occurred while deleting book",
                        "Deletion Failed", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_DeleteBookBtnActionPerformed

    private void UpdateBookBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateBookBtnActionPerformed
        String ISBN = isbnTF.getText().trim();
        String bookName = bnTF.getText().trim();
        String category = (String) catCMB.getSelectedItem();
        String authorName = anTF.getText().trim();
        String copies = copiesTF.getText().trim();

        if (ISBN.isEmpty() || bookName.isEmpty() || category.isEmpty() ||
            authorName.isEmpty() || copies.isEmpty()) {
            JOptionPane.showMessageDialog(null, "All fields must be filled out",
                "Input Error", JOptionPane.ERROR_MESSAGE);
        } else {
            boolean success = false;
            try {
                success = BookController.updateBook(ISBN, bookName, authorName, category, copies);
            } catch (SQLException ex) {
                    ex.printStackTrace();
            }
            if (success) {
                JOptionPane.showMessageDialog(null, "Book information updated successfully",
                    "Update Successful", JOptionPane.INFORMATION_MESSAGE);
                clearField();
            } else {
                JOptionPane.showMessageDialog(null, "Failed to book book information",
                    "Update Failed", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_UpdateBookBtnActionPerformed
    
